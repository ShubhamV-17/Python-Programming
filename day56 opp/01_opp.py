# class is a blue print to creating an object
class employee:
    
    company="microsoft"
    language="java"
    salary=1000000
shubham=employee()
shubham.name="sameeksha robionason"
shubham.company="google"
print(shubham.name,shubham.company,shubham.language,shubham.salary,end=",")
harry=employee()
harry.name="mickel jonson" 
print(harry.name,harry.company,harry.salary,harry.language)
# here name is a object/instance attribute and language, salary,company are class attribute as they belong to the class
